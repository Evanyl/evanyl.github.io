import Brick from "./brick.js";

export function buildLevel(level) {
	let bricks = [];
	let position = {};
	level.forEach((row, rowIndex) => {
		row.forEach((brick, brickIndex) => {
			if (brick === 1) {
				position = {
					x: 80 * brickIndex,
					y: 20 * rowIndex
				}
				bricks.push(new Brick(position));
			}
		});
	});

	return bricks;
}


